# ---------- ERROR ANALYSIS / AUTO-FIX ----------

def analyze_and_fix_errors(project: str, errors: str, events: List[AgentEvent]) -> None:
    root = project_root(project)
    ensure_dir(root)
    write_file(
        os.path.join(root, "diagnostics.log"),
        errors,
    )
    write_file(
        os.path.join(root, "diagnostics.md"),
        f"# Diagnostics for {project}\n\n```\n{errors}\n```\n\nNext steps:\n- Inspect diagnostics.log\n- Adjust code or configs accordingly.\n",
    )
    events.append(AgentEvent(kind="agent", message="Error log captured to diagnostics.log and diagnostics.md for this project."))


# ---------- AGENT LOOP ----------

def run_agent(scope: AgentScope) -> AgentRunResponse:
    events: List[AgentEvent] = []
    events.append(AgentEvent(kind="system", message=f"Agent started in {scope.mode} mode for project '{scope.project}'."))

    if scope.allow_scaffold:
        scaffold_fullstack(scope.stack, scope.project, events)

    if scope.allow_db:
        create_db_layer(scope.project, events)

    if scope.allow_api:
        create_api_layer(scope.project, events)

    if scope.allow_auth:
        create_auth_layer(scope.project, events)

    if scope.allow_media:
        create_media_layer(scope.project, events)

    if scope.allow_cloudflare:
        create_cloudflare_layer(scope.project, events)

    if scope.allow_frontend or scope.allow_service_worker:
        create_frontend_layer(scope.project, events)

    if scope.allow_deploy:
        create_deploy_layer(scope.project, events)

    summary = f"Agent completed for project '{scope.project}' with stack '{scope.stack}'."
    events.append(AgentEvent(kind="system", message=summary))
    return AgentRunResponse(events=events, summary=summary)


# ---------- ENDPOINTS ----------

@app.post("/api/agent/run", response_model=AgentRunResponse)
def api_agent_run(scope: AgentScope):
    return run_agent(scope)


@app.post("/api/agent/fix_errors", response_model=FixErrorsResponse)
def api_fix_errors(req: FixErrorsRequest):
    events: List[AgentEvent] = []
    events.append(AgentEvent(kind="system", message=f"Received error log for project '{req.project}'."))
    analyze_and_fix_errors(req.project, req.errors, events)
    summary = f"Diagnostics written for project '{req.project}'."
    events.append(AgentEvent(kind="system", message=summary))
    return FixErrorsResponse(events=events, summary=summary)


