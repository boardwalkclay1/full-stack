# backend/main.py
import os
import shutil
from typing import List, Literal, Optional

from fastapi import FastAPI
from fastapi.middleware.cors import CORSMiddleware
from pydantic import BaseModel

BASE_DIR = os.path.dirname(os.path.abspath(__file__))
FILES_DIR = os.path.join(BASE_DIR, "boardwalk_files")
os.makedirs(FILES_DIR, exist_ok=True)

app = FastAPI()
app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)


# ---------- MODELS ----------

class AgentScope(BaseModel):
    project: str
    goal: str
    stack: str
    allow_scaffold: bool
    allow_db: bool
    allow_api: bool
    allow_auth: bool
    allow_media: bool
    allow_cloudflare: bool
    allow_frontend: bool
    allow_service_worker: bool
    allow_deploy: bool
    allow_fix: bool
    mode: Literal["full", "limited"]


class AgentEvent(BaseModel):
    kind: str
    message: str


class AgentRunResponse(BaseModel):
    events: List[AgentEvent]
    summary: str


class FixErrorsRequest(BaseModel):
    project: str
    errors: str


class FixErrorsResponse(BaseModel):
    events: List[AgentEvent]
    summary: str


# ---------- HELPERS ----------

def safe_name(name: str) -> str:
    return name.replace("..", "_").strip().replace(" ", "-")


def project_root(project: str) -> str:
    return os.path.join(FILES_DIR, safe_name(project))


def ensure_dir(path: str) -> None:
    os.makedirs(path, exist_ok=True)


def write_file(path: str, content: str) -> None:
    ensure_dir(os.path.dirname(path))
    with open(path, "w", encoding="utf-8") as f:
        f.write(content)


